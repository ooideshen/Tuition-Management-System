    private Users validateLogin(String id, String password) {
        String filename;

        if (id.startsWith("ST")) {
            filename = "students.txt";
        } else if (id.startsWith("TT")) {
            filename = "tutors.txt";
        } else if (id.startsWith("AD")) {
            filename = "admins.txt";
        } else if (id.startsWith("RP")) {
            filename = "receptionists.txt";
        } else {
            return null;
        }

        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("::");

                if (parts[0].trim().equals(id) && parts[1].trim().equals(password)) {
                    String name = parts[2].trim();
                    String email = parts[3].trim();
                    String phone = parts[4].trim();

                    switch (filename) {
                        case "students.txt":
                            String ic = parts[3].trim();
                            String studentEmail = parts[4].trim();
                            String studentPhone = parts[5].trim();
                            String address = parts[6].trim();
                            String level = parts[7].trim();
                            return new Student(id, password, name, studentEmail, studentPhone, ic, level, address);
                        case "tutors.txt":
                            return new Tutor(id, password, name, email, phone);
                        case "admins.txt":
                            return new Admin(id, password, name, email, phone);
                        case "receptionists.txt":
                            return new Receptionist(id, password, name, email, phone);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error reading " + filename, "File Error",
                    JOptionPane.ERROR_MESSAGE);
        }

        return null;
    }

