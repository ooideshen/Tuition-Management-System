    private int loginAttempts = 0;
    private static final int MAX_ATTEMPTS = 3;

    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        String id = tfID.getText().trim();
        String password = new String(tfPassword.getPassword()).trim();

        Users user = validateLogin(id, password);

        if (user != null) {
            loginAttempts = 0;

            if (user instanceof Student) {
                openStudentPortal((Student) user);
            } else if (user instanceof Tutor) {
                openTutorPortal((Tutor) user);
            } else if (user instanceof Admin) {
                openAdminPortal((Admin) user);
            } else if (user instanceof Receptionist) {
                openReceptionistPortal((Receptionist) user);
            }

        } else {
            loginAttempts++;
            if (loginAttempts >= MAX_ATTEMPTS) {
                JOptionPane.showMessageDialog(this,
                        "Too many failed attempts. Exiting...",
                        "Login Failed", JOptionPane.ERROR_MESSAGE);
                System.exit(0);

            } else {
                JOptionPane.showMessageDialog(this,
                        "Invalid ID or password. Attempts remaining: " + (MAX_ATTEMPTS - loginAttempts),
                        "Login Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

