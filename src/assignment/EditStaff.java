    private void applyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyBtnActionPerformed

        String name = tfName.getText().trim();
        String email = tfEmail.getText().trim();
        String phone = tfPhone.getText().trim();

        if (name.isEmpty() || email.isEmpty() || phone.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "All fields must be filled.",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!phone.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this,
                    "Phone number must be exactly 10 digits.",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            JOptionPane.showMessageDialog(this,
                    "Email format is invalid.",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        user.setName(name);
        user.setEmail(email);
        user.setPhone(phone);

        DefaultTableModel model = (DefaultTableModel) profileTable.getModel();
        model.setValueAt(user.getName(), 0, 1);
        model.setValueAt(user.getEmail(), 0, 2);
        model.setValueAt(user.getPhone(), 0, 3);

        JOptionPane.showMessageDialog(this,
                "Changes applied! Don’t forget to Save Changes.",
                "Success",
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_applyBtnActionPerformed

