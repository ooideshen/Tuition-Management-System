    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        if (user instanceof Tutor) {
            TutorPortal tp = new TutorPortal((Tutor) user);
            tp.setVisible(true);
        } else if (user instanceof Admin) {
            AdminDashboard ap = new AdminDashboard((Admin) user);
            ap.setVisible(true);
        } else if (user instanceof Receptionist) {
            ReceptionistPortal rp = new ReceptionistPortal((Receptionist) user);
            rp.setVisible(true);
        } else {
            // fallback — maybe show an error or go to a default screen
            JOptionPane.showMessageDialog(this,
                    "Unknown user type. Cannot go back to portal.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_backBtnActionPerformed

