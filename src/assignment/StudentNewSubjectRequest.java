    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        String type = jComboBoxRequestType.getSelectedItem().toString();
        String change = jTextFieldSubjectChange.getText().trim();
        String reason = jTextAreaReason.getText().trim();

        if (change.isEmpty() || reason.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        String newID = generateNewRequestID();
        String today = LocalDate.now().format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        String currentSubjects = String.join(",", currentStudent.getSubjects());

        String line = String.join("::",
                newID,
                currentStudent.getId(),
                currentStudent.getName(),
                currentSubjects,
                type,
                change,
                reason,
                "Pending",
                today
        );

        try (BufferedWriter bw = new BufferedWriter(new FileWriter(FILE_NAME, true))) {
            bw.write(line);
            bw.newLine();
            JOptionPane.showMessageDialog(this, "Request submitted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing to file:\n" + e.getMessage());
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

