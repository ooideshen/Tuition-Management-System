    private String generateNewRequestID() {
        int max = 0;
        try (BufferedReader br = new BufferedReader(new FileReader(FILE_NAME))) {
            String line;
            while ((line = br.readLine()) != null) {
                if (line.startsWith("REQ")) {
                    try {
                        int id = Integer.parseInt(line.substring(3, 6));
                        if (id > max) max = id;
                    } catch (NumberFormatException ignored) {}
                }
            }
        } catch (IOException e) {
            System.out.println("Unable to read for ID generation.");
        }
        return String.format("REQ%03d", max + 1);
    }
