    private List<String> getSubjectsFromFile(List<String> studentSubjectsId) {
        List<String> subjectList = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader("subjects.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("::");
                if (parts.length >= 3) {
                    String subjectsId = parts[1].trim();     // e.g., SJ1001
                    String subjects = parts[2].trim();  // e.g., Mathematics

                    if (studentSubjectsId.contains(subjectsId)) {
                        subjectList.add(subjects); // Or add subjCode + " - " + subjectName
                    }
                }
            }
        } catch (IOException e) {
            System.err.println("Error reading subjects.txt: " + e.getMessage());
        }

        return subjectList;
    }
    
