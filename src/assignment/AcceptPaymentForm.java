    private void searchStudent() {
        String studentId = studentIdField.getText().trim();
        
        if (studentId.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Please enter a student ID!",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Find student
        List<Student> students = FileHandler.readStudents();
        currentStudent = null;
        
        for (Student student : students) {
            if (student.getId().equals(studentId)) {
                currentStudent = student;
                break;
            }
        }
        
        if (currentStudent == null) {
            JOptionPane.showMessageDialog(this,
                "Student not found!",
                "Search Error",
                JOptionPane.ERROR_MESSAGE);
            resetForm();
            return;
        }
        
        // Find enrollment
        List<Enrollment> enrollments = FileHandler.readEnrollments();
        Enrollment studentEnrollment = null;
        
        for (Enrollment enrollment : enrollments) {
            if (enrollment.getStudentId().equals(studentId)) {
                studentEnrollment = enrollment;
                break;
            }
        }
        
        if (studentEnrollment == null) {
            JOptionPane.showMessageDialog(this,
                "No enrollment found for this student!",
                "Search Error",
                JOptionPane.ERROR_MESSAGE);
            resetForm();
            return;
        }
        
        // Get enrolled subjects
        List<ClassInfo> allClasses = FileHandler.readClasses();
        enrolledSubjects = new ArrayList<>();
        List<String> enrolledSubjectIds = new ArrayList<>();
        
        if (studentEnrollment.getSubjectId1() != null && !studentEnrollment.getSubjectId1().isEmpty()) {
            enrolledSubjectIds.add(studentEnrollment.getSubjectId1());
        }
        if (studentEnrollment.getSubjectId2() != null && !studentEnrollment.getSubjectId2().isEmpty()) {
            enrolledSubjectIds.add(studentEnrollment.getSubjectId2());
        }
        if (studentEnrollment.getSubjectId3() != null && !studentEnrollment.getSubjectId3().isEmpty()) {
            enrolledSubjectIds.add(studentEnrollment.getSubjectId3());
        }
        
        // Find subject details and calculate total fee
        totalFee = 0;
        StringBuilder subjectsText = new StringBuilder();
        
        for (String subjectId : enrolledSubjectIds) {
            for (ClassInfo classInfo : allClasses) {
                if (classInfo.getSubjectId().equals(subjectId)) {
                    enrolledSubjects.add(classInfo);
                    totalFee += classInfo.getMonthlyCost();
                    subjectsText.append("• ").append(classInfo.getSubjectName())
                               .append(" - ").append(classInfo.getDay())
                               .append(" ").append(classInfo.getStartTime())
                               .append("-").append(classInfo.getEndTime())
                               .append(" (RM").append(classInfo.getMonthlyCost()).append(")\n");
                    break;
                }
            }
        }
        
        // Update display
        studentNameLabel.setText(currentStudent.getName());
        enrolledSubjectsArea.setText(subjectsText.toString());
        totalFeeLabel.setText("RM " + String.format("%.2f", totalFee));
        amountPaidField.setText("");
        amountPaidField.requestFocus();
    }
    
    private void resetForm() {
        studentNameLabel.setText("-");
        enrolledSubjectsArea.setText("");
        totalFeeLabel.setText("RM 0.00");
        amountPaidField.setText("");
        currentStudent = null;
        enrolledSubjects = null;
        totalFee = 0;
    }
    
    private void acceptPayment() {
        if (currentStudent == null) {
            JOptionPane.showMessageDialog(this,
                "Please search for a student first!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String amountPaidStr = amountPaidField.getText().trim();
        
        if (amountPaidStr.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Please enter the amount paid!",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        double amountPaid;
        try {
            amountPaid = Double.parseDouble(amountPaidStr);
            if (amountPaid <= 0) {
                throw new NumberFormatException("Negative amount");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Please enter a valid amount!",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Generate payment ID and date
        String paymentId = FileHandler.generateNextPaymentId();
        String paymentDate = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());

        // Create payment record
        Payment payment = new Payment(paymentId, currentStudent.getId(), amountPaid, paymentDate);

        if (FileHandler.appendPayment(payment)) {
            // Generate receipt
            generateReceipt(payment);

            JOptionPane.showMessageDialog(this,
                "Payment accepted successfully!",
                "Success",
                JOptionPane.INFORMATION_MESSAGE);

            resetForm();
        } else {
            JOptionPane.showMessageDialog(this,
                "Error saving payment!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void generateReceipt(Payment payment) {
        String receiptContent = buildReceiptContent(payment);
        
        // Display receipt with print option
        JTextArea receiptArea = new JTextArea(receiptContent);
        receiptArea.setEditable(false);
        receiptArea.setFont(new Font(Font.MONOSPACED, Font.PLAIN, 12));
        
        JScrollPane scrollPane = new JScrollPane(receiptArea);
        scrollPane.setPreferredSize(new Dimension(400, 500));
        
        // Create custom dialog with print and cancel buttons
        JDialog receiptDialog = new JDialog(this, "Payment Receipt", true);
        receiptDialog.setLayout(new BorderLayout());
        receiptDialog.add(scrollPane, BorderLayout.CENTER);
        
        // Button panel
        JPanel buttonPanel = new JPanel(new FlowLayout());
        JButton printButton = new JButton("Download and Print Receipt");
        JButton closeButton = new JButton("Cancel");
        
        printButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                printReceipt(receiptContent, payment.getPaymentId());
            }
        });
        
        closeButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                receiptDialog.dispose();
            }
        });
        
        buttonPanel.add(printButton);
        buttonPanel.add(closeButton);
        receiptDialog.add(buttonPanel, BorderLayout.SOUTH);
        
        receiptDialog.setSize(450, 600);
        receiptDialog.setLocationRelativeTo(this);
        receiptDialog.setVisible(true);
    }

    private String buildReceiptContent(Payment payment) {
        StringBuilder receipt = new StringBuilder();
        receipt.append("=====================================\n");
        receipt.append("       TUITION CENTRE RECEIPT        \n");
        receipt.append("=====================================\n\n");
        receipt.append("Receipt No: ").append(payment.getPaymentId()).append("\n");
        receipt.append("Date: ").append(payment.getDateOfPayment()).append("\n\n");
        receipt.append("Student ID: ").append(currentStudent.getId()).append("\n");
        receipt.append("Student Name: ").append(currentStudent.getName()).append("\n\n");
        receipt.append("Subjects Covered:\n");
        receipt.append("----------------------------------\n");
        
        for (ClassInfo classInfo : enrolledSubjects) {
            receipt.append("• ").append(classInfo.getSubjectName())
                   .append(" (RM").append(classInfo.getMonthlyCost()).append(")\n");
        }
        
        receipt.append("\n----------------------------------\n");
        receipt.append("Total Monthly Fee: RM ").append(String.format("%.2f", totalFee)).append("\n");
        receipt.append("Amount Paid: RM ").append(String.format("%.2f", payment.getAmountPaid())).append("\n");
        
        if (payment.getAmountPaid() < totalFee) {
            double balance = totalFee - payment.getAmountPaid();
            receipt.append("Balance Due: RM ").append(String.format("%.2f", balance)).append("\n");
        } else if (payment.getAmountPaid() > totalFee) {
            double excess = payment.getAmountPaid() - totalFee;
            receipt.append("Excess Payment: RM ").append(String.format("%.2f", excess)).append("\n");
        }
        
        receipt.append("\n=====================================\n");
        receipt.append("    Thank you for your payment!      \n");
        receipt.append("=====================================\n");
        
        return receipt.toString();
    }
    
    private void downloadReceipt(String receiptContent, String paymentId) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Save Receipt");
        fileChooser.setSelectedFile(new java.io.File("Receipt_" + paymentId + ".txt"));
        
        // Set file filter to show only text files
        fileChooser.setFileFilter(new javax.swing.filechooser.FileFilter() {
            @Override
            public boolean accept(java.io.File file) {
                return file.isDirectory() || file.getName().toLowerCase().endsWith(".txt");
            }
            
            @Override
            public String getDescription() {
                return "Text Files (*.txt)";
            }
        });
        
        int result = fileChooser.showSaveDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            java.io.File selectedFile = fileChooser.getSelectedFile();
            
            // Ensure the file has .txt extension
            String filePath = selectedFile.getAbsolutePath();
            if (!filePath.toLowerCase().endsWith(".txt")) {
                filePath += ".txt";
                selectedFile = new java.io.File(filePath);
            }
            
            try (java.io.PrintWriter writer = new java.io.PrintWriter(selectedFile)) {
                writer.print(receiptContent);
                JOptionPane.showMessageDialog(this,
                    "Receipt saved successfully to:\n" + selectedFile.getAbsolutePath(),
                    "Download Complete",
                    JOptionPane.INFORMATION_MESSAGE);
            } catch (java.io.IOException e) {
                JOptionPane.showMessageDialog(this,
                    "Error saving receipt:\n" + e.getMessage(),
                    "Download Error",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
