    private void printReceipt(String receiptContent, String paymentId) {
        try {
            // Create a printable object
            Printable printable = new Printable() {
                @Override
                public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
                    if (pageIndex > 0) {
                        return NO_SUCH_PAGE;
                    }
                    
                    Graphics2D g2d = (Graphics2D) graphics;
                    g2d.translate(pageFormat.getImageableX(), pageFormat.getImageableY());
                    
                    // Set font and color
                    g2d.setFont(new Font(Font.MONOSPACED, Font.PLAIN, 10));
                    g2d.setColor(java.awt.Color.BLACK);
                    
                    // Split content into lines
                    String[] lines = receiptContent.split("\n");
                    int y = 20;
                    int lineHeight = 15;
                    
                    for (String line : lines) {
                        if (y > pageFormat.getImageableHeight() - 50) {
                            break; // Don't print beyond page
                        }
                        g2d.drawString(line, 20, y);
                        y += lineHeight;
                    }
                    
                    return PAGE_EXISTS;
                }
            };
            
            // Create print job
            PrinterJob job = PrinterJob.getPrinterJob();
            job.setPrintable(printable);
            
            if (job.printDialog()) {
                job.print();
                JOptionPane.showMessageDialog(this,
                    "Receipt printed successfully!",
                    "Print Complete",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (PrinterException e) {
            JOptionPane.showMessageDialog(this,
                "Error printing receipt:\n" + e.getMessage(),
                "Print Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }
        
        
    
