    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed

        DefaultTableModel model = (DefaultTableModel) classTable.getModel();

        int selectedRow = classTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Please select a class to delete.",
                    "No Selection",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete the selected class?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(
                    this,
                    "Class deleted from the table. Remember to save to apply changes.",
                    "Deleted",
                    JOptionPane.INFORMATION_MESSAGE
            );
        }
    }//GEN-LAST:event_delBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) classTable.getModel();
        List<String[]> rows = new ArrayList<>();
        String tutorID = tutor.getId();

        for (int i = 0; i < model.getRowCount(); i++) {
            String[] row = new String[6];
            for (int j = 0; j < 6; j++) {
                row[j] = model.getValueAt(i, j).toString();
            }
            rows.add(row);
        }

        txtManagementFunctions.updateTutorClasses(tutorID, rows, "class.txt");
    }//GEN-LAST:event_saveBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String className = (String) nameBox.getSelectedItem();
        String day = (String) dayBox.getSelectedItem();
        String startTimeStr = (String) startTimeBox.getSelectedItem();
        String endTimeStr = (String) endTimeBox.getSelectedItem();
        String costStr = tfCost.getText().trim();

        if (className == null || className.isEmpty()
                || day == null || day.isEmpty()
                || startTimeStr == null || startTimeStr.isEmpty()
                || endTimeStr == null || endTimeStr.isEmpty()
                || costStr.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Missing Data", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validate cost
        double cost;
        try {
            cost = Double.parseDouble(costStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Cost must be a valid number.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate time order
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm");
        timeFormat.setLenient(false);

        try {
            Date startTime = timeFormat.parse(startTimeStr);
            Date endTime = timeFormat.parse(endTimeStr);

            if (!startTime.before(endTime)) {
                JOptionPane.showMessageDialog(this, "End time must be after start time.", "Invalid Time", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Invalid time format.", "Invalid Time", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Build txtData (current table data) to generate next ID
        DefaultTableModel model = (DefaultTableModel) classTable.getModel();
        List<String[]> txtData = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            String[] row = new String[model.getColumnCount()];
            for (int j = 0; j < model.getColumnCount(); j++) {
                row[j] = String.valueOf(model.getValueAt(i, j));
            }
            txtData.add(row);
        }

        File classFile = new File("class.txt");
        String classId = txtManagementFunctions.generateID(classFile);

        model.addRow(new Object[]{
            classId,
            className,
            day,
            startTimeStr,
            endTimeStr,
            String.format("%.2f", cost)
        });

        resetFields();

        JOptionPane.showMessageDialog(this, "Class added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_addBtnActionPerformed

