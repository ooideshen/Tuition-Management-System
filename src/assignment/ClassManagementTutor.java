    public void loadClassTutor() {
        DefaultTableModel model = (DefaultTableModel) classTable.getModel();
        model.setRowCount(0); // Clear table

        String filename = "class.txt";
        String tutorID = tutor.getId();

        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("::");
                if (parts.length < 7) {
                    continue;
                }

                if (parts[0].trim().equals(tutorID)) {
                    // Extract fields
                    String classID = parts[1].trim();
                    String subjectName = parts[2].trim();
                    String day = parts[3].trim();
                    String startTime = parts[4].trim();
                    String endTime = parts[5].trim();
                    String cost = parts[6].trim();

                    // Add to table
                    model.addRow(new Object[]{classID, subjectName, day, startTime, endTime, cost});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error reading classes file: " + e.getMessage());
        }
    }

