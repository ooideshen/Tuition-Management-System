    private void applyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyBtnActionPerformed
        int selectedRow = classTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to apply changes.");
            return;
        }

        String name = (String) nameBox.getSelectedItem();
        String day = (String) dayBox.getSelectedItem();
        String startTime = (String) startTimeBox.getSelectedItem();
        String endTime = (String) endTimeBox.getSelectedItem();
        String cost = tfCost.getText().trim();

        if (name == null || day == null || startTime == null || endTime == null || cost.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        // Check start < end
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm");
        try {
            Date start = timeFormat.parse(startTime);
            Date end = timeFormat.parse(endTime);
            if (!end.after(start)) {
                JOptionPane.showMessageDialog(this, "End time must be after start time.");
                return;
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid time format in combo boxes.");
            return;
        }

        classTable.setValueAt(name, selectedRow, 1);
        classTable.setValueAt(day, selectedRow, 2);
        classTable.setValueAt(startTime, selectedRow, 3);
        classTable.setValueAt(endTime, selectedRow, 4);
        classTable.setValueAt(cost, selectedRow, 5);

        resetFields();
        applyBtn.setEnabled(false);
    }//GEN-LAST:event_applyBtnActionPerformed

