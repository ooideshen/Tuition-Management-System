    private void loadRequests() {
        DefaultTableModel model = (DefaultTableModel) jTableRequests.getModel();
        model.setRowCount(0);

        try (BufferedReader br = new BufferedReader(new FileReader(FILE_NAME))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split("::");
                if (parts.length >= 9 && parts[1].equalsIgnoreCase(currentStudent.getId())) {
                    model.addRow(new Object[]{
                            parts[0], // Request ID
                            parts[4], // Type
                            parts[5], // Requested Change
                            parts[6], // Reason
                            parts[7], // Status
                            parts[8]  // Date
                    });
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error loading requests:\n" + e.getMessage());
        }
    }

