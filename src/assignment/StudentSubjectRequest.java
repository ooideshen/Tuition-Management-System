    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int selectedRow = jTableRequests.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a request to delete.");
            return;
        }

        String status = (String) jTableRequests.getValueAt(selectedRow, 4);
        if (!status.equalsIgnoreCase("Pending")) {
            JOptionPane.showMessageDialog(this, "Only pending requests can be deleted.");
            return;
        }

        String requestId = (String) jTableRequests.getValueAt(selectedRow, 0);
        List<String> updatedLines = new ArrayList<>();

        try (BufferedReader br = new BufferedReader(new FileReader(FILE_NAME))) {
            String line;
            while ((line = br.readLine()) != null) {
                if (!line.startsWith(requestId + "::")) {
                    updatedLines.add(line);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading request file.");
            return;
        }

        try (BufferedWriter bw = new BufferedWriter(new FileWriter(FILE_NAME))) {
            for (String s : updatedLines) {
                bw.write(s);
                bw.newLine();
            }
            JOptionPane.showMessageDialog(this, "Request deleted.");
            loadRequests();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing to request file.");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

